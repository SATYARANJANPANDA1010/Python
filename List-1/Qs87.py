# Nested list
# defining list inside list is called nested list.
# Nested list is used to organize data in rows and columns.

list1 = [[10, 20, 30],[40, 50, 60],[70, 80, 90]]
print(list1[0])
print(list1[1])
print(list1[2])

print(list1[0][0])
print(list1[0][1])
print(list1[0][2])

print(list1[1][0])
print(list1[1][1])
print(list1[1][2])

print(list1[2][0])
print(list1[2][1])
print(list1[2][2])

