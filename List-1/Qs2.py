# Creating list with items separated with , (comma).

# list can store homogeneous items
list1 = [10, 20, 30, 40, 50]
list2 = [1.5, 1.6, 1.7]
list3 = [1+2j, 1+1j, 1+3j]
list4 = ["naresh", "satya", "ramesh"]
# list can store heterogeneous items
list5 = [10, 1.5, 1+2j, "python"]

print(list1, list2, list3, list4, list5)


