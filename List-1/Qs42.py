# Ways to find length of list
test_list = [1, 4, 5, 7, 8]
count = 0
for i in test_list:
    count = count+1
print(f'The length of the list is {count}')

# using pre-defined len()
length_test_list = len(test_list)
print(f'Length of the list is {length_test_list}')

# len()
n = len([10, 20, 30, 40, 50, 60])
print(f'Length of the list is {n}')

